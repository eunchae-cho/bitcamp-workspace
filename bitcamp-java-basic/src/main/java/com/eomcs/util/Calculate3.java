package com.eomcs.util;

public class Calculate3 {

  public int result;

  public void plus(int value) {
    result += value;
  }

  public void minus(int value) {
    result -= value;
  }

  public void multiple(int value) {
    result *= value;
  }

  public void divide(int value) {
    result /= value;
  }
}
