package com.eomcs.basic.ex05;
// 비트 연산자

public class Exam0350_test {
  public static void main(String[] args) {
    int a = 0b110_1100; // 108
    int b = 0b101_0101; // 85
    
//    System.out.println(a && b);
//    System.out.println(a || b);
//    System.out.println(!a);
    // 정수 값에 대해서는 논리연산자를 사용할 수 없다.
    
    // 하지만 비트연산자는 사용 가능하다.
    // 1 = true 
    // 0 = false
    
    System.out.println(a & b);
    // a = 00000000 00000000 00000000 01101100
    // b = 00000000 00000000 00000000 01010101
    // -----------------------------------------
    //     00000000 00000000 00000000 01000100 = 68
    
    System.out.println(a | b);
    // a = 00000000 00000000 00000000 01101100
    // b = 00000000 00000000 00000000 01010101
    // ------------------------------------------
    //     00000000 00000000 00000000 01111101 = 125
    
    System.out.println(a ^ b);
    // a = 00000000 00000000 00000000 01101100
    // b = 00000000 00000000 00000000 01010101
    // ------------------------------------------
    //     00000000 00000000 00000000 00111001 = 57
    
    System.out.println(~a);
    // a = 00000000 00000000 00000000 00101100
    // ------------------------------------------
    //     11111111 11111111 11111111 11010011 = -109
  }
}
